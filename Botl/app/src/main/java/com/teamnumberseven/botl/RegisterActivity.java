    /**
     * Shows the progress UI and hides the login form.
     */
    @TargetApi(Build.VERSION_CODES.HONEYCOMB_MR2)
    private void showProgress(final boolean show) {
        // On Honeycomb MR2 we have the ViewPropertyAnimator APIs, which allow
        // for very easy animations. If available, use these APIs to fade-in
        // the progress spinner.
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.HONEYCOMB_MR2) {
            int shortAnimTime = getResources().getInteger(android.R.integer.config_shortAnimTime);

            mRegisterFormView.setVisibility(show ? View.GONE : View.VISIBLE);
            mRegisterFormView.animate().setDuration(shortAnimTime).alpha(
                    show ? 0 : 1).setListener(new AnimatorListenerAdapter() {
                @Override
                public void onAnimationEnd(Animator animation) {
                    mRegisterFormView.setVisibility(show ? View.GONE : View.VISIBLE);
                }
            });

            mProgressView.setVisibility(show ? View.VISIBLE : View.GONE);
            mProgressView.animate().setDuration(shortAnimTime).alpha(
                    show ? 1 : 0).setListener(new AnimatorListenerAdapter() {
                @Override
                public void onAnimationEnd(Animator animation) {
                    mProgressView.setVisibility(show ? View.VISIBLE : View.GONE);
                }
            });
        } else {
            // The ViewPropertyAnimator APIs are not available, so simply show
            // and hide the relevant UI components.
            mProgressView.setVisibility(show ? View.VISIBLE : View.GONE);
            mRegisterFormView.setVisibility(show ? View.GONE : View.VISIBLE);
        }
    }

    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mUsername;
        private final String mPassword;
        private final String mPhone;
        private final String mDeviceID;

        UserLoginTask(String username, String password, String phone) {
            mUsername = username;
            mPassword = password;
            mPhone = phone;
            mDeviceID = Secure.getString(RegisterActivity.this.getContentResolver(), Secure.ANDROID_ID);
        }

        @Override
        protected Boolean doInBackground(Void... params) {
            // TODO: attempt authentication against a network service.

            // Simulate network access.
            final String URL = "http://bttl.herokuapp.com/api/verify";
            RequestQueue queue = Volley.newRequestQueue(RegisterActivity.this);
            HashMap<String,String> parameters = new HashMap<String,String>();
            parameters.put("username", mUsername);
            //parameters.put("password", mPassword);
            parameters.put("phone", mPhone);
            parameters.put("device", mDeviceID);

            JsonObjectRequest req = new JsonObjectRequest(URL, new JSONObject(parameters),
                    new Response.Listener<JSONObject>() {
                        @Override
                        public void onResponse(JSONObject response) {
                            try {
                                VolleyLog.v("Response:%n %s", response.toString(4));
                                Log.d("RESP:" , response.toString(4));
                                Log.d("PARAM:", mUsername);
                                Log.d("PARAM:", mPassword);
                                Log.d("PARAM:", mPhone);
                                Log.d("PARAM:", mDeviceID);
                                Log.d("FXN:", mUsername);
                                Log.d("FXN:", mPassword);
                                Log.d("FXN:", mPhone);
                                Log.d("FXN:", mDeviceID);
                                Intent intent = new Intent(getApplicationContext(), RegistrationVerification.class);
                                intent.putExtra("username", mUsername);
                                intent.putExtra("password", mPassword);
                                intent.putExtra("phone", mPhone);
                                intent.putExtra("device", mDeviceID);
                                startActivity(intent);
                                overridePendingTransition(R.animator.enter_from_login, R.animator.exit_from_login);
                            }
                            catch (JSONException e) {
                                e.printStackTrace();
                            }
                        }
                    }, new Response.ErrorListener() {
                @Override
                public void onErrorResponse(VolleyError error) {
                    VolleyLog.e("Error: ", error.getMessage());
                }
            });
            queue.add(req);
            queue.start();


            // TODO: register the new account here.
            return true;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                finish();
            } else {
                mPasswordView.setError(getString(R.string.error_incorrect_login));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
